import UIKit

extension UIView {
    
    static var kScreenWidth:CGFloat {
        get { return UIScreen.main.bounds.size.width; }
    }
    
    static var kScreenHeight:CGFloat {
        get { return UIScreen.main.bounds.size.height; }
    }
}
